package fr.cnes.regards.modules.indexer.domain.criterion;

/**
 * Property criterion. Provides a property name
 *
 * @author oroussel
 */
public abstract class AbstractPropertyCriterion implements ICriterion {

    /**
     * Concerned property name
     */
    protected String name;

    public AbstractPropertyCriterion(String name) {
        super();
        this.name = name;
    }

    /**
     * @return {@link #name}
     */
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + (name == null ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null) {
            return false;
        }
        if (getClass() != o.getClass()) {
            return false;
        }
        AbstractPropertyCriterion other = (AbstractPropertyCriterion) o;
        if (name == null) {
            if (other.name != null) {
                return false;
            }
        } else if (!name.equals(other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return this.getClass().getName() + " on property " + name;
    }
}
