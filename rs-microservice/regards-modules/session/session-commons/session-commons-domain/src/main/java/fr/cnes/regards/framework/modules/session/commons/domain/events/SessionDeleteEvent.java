package fr.cnes.regards.framework.modules.session.commons.domain.events;

import fr.cnes.regards.framework.amqp.event.Event;
import fr.cnes.regards.framework.amqp.event.ISubscribable;
import fr.cnes.regards.framework.amqp.event.JsonMessageConverter;
import fr.cnes.regards.framework.amqp.event.Target;

import java.util.Objects;

/**
 * Event to delete a session
 *
 * @author Iliana Ghazali
 **/
@Event(target = Target.ONE_PER_MICROSERVICE_TYPE, converter = JsonMessageConverter.GSON)
public class SessionDeleteEvent implements ISubscribable {

    /**
     * Source including the session to delete
     */
    private String source;

    /**
     * Session to be deleted
     */
    private String session;

    public SessionDeleteEvent(String source, String session) {
        this.source = source;
        this.session = session;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getSession() {
        return session;
    }

    public void setSession(String session) {
        this.session = session;
    }

    @Override
    public String toString() {
        return "SessionDeleteEvent{" + "source='" + source + '\'' + ", session='" + session + '\'' + '}';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        SessionDeleteEvent that = (SessionDeleteEvent) o;
        return Objects.equals(source, that.source) && Objects.equals(session, that.session);
    }

    @Override
    public int hashCode() {
        return Objects.hash(source, session);
    }
}
