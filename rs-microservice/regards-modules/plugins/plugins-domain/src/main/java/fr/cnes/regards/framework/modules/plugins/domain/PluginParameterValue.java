package fr.cnes.regards.framework.modules.plugins.domain;

import javax.persistence.Convert;

/**
 * This class represents a parameter value. It is only used to attach specific converter and adapter.
 *
 * @author Marc Sordi
 *
 */
@Convert(converter = PluginParameterValueConverter.class)
public class PluginParameterValue {

    private String value;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = (prime * result) + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        PluginParameterValue other = (PluginParameterValue) obj;
        if (value == null) {
            if (other.value != null) {
                return false;
            }
        } else if (!value.equals(other.value)) {
            return false;
        }
        return true;
    }

    public static PluginParameterValue create(String value) {
        PluginParameterValue ppv = new PluginParameterValue();
        ppv.setValue(value);
        return ppv;
    }
}
